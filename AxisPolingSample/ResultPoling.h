#pragma once

#include "Axis/ResultPoling.h"

namespace Standard
{
	namespace Axis
	{
		namespace Result
		{
			////////////////////////////////////////////////////////////////////////////////
			/// @class      CInitialize
			/// @brief      軸を初期化した結果クラス
			///				⇒ 軸を監視した結果のスーパークラスから継承
			////////////////////////////////////////////////////////////////////////////////
			class CInitialize
				: virtual public Result::CSuper
			{
			public:
				////////////////////////////////////////////////////////////////////////////////
				/// @brief			コンストラクタ
				////////////////////////////////////////////////////////////////////////////////
				CInitialize()
				{

				}

				////////////////////////////////////////////////////////////////////////////////
				/// @brief			コピー代入演算子
				/// @return			自クラス
				////////////////////////////////////////////////////////////////////////////////
				CInitialize& operator = (const CInitialize& object) noexcept
				{
					// 更新
					Update(object);

					return *this;
				}

				////////////////////////////////////////////////////////////////////////////////
				/// @brief			デストラクタ
				////////////////////////////////////////////////////////////////////////////////
				virtual ~CInitialize()
				{

				}

			protected:
				////////////////////////////////////////////////////////////////////////////////
				/// @brief			自クラスを更新
				/// @param[in]		object	自クラス
				////////////////////////////////////////////////////////////////////////////////
				void Update(const CInitialize& object)
				{
					// 既定の関数
					CSuper::Update(object);
				}
			};

			////////////////////////////////////////////////////////////////////////////////
			/// @class      CServo
			/// @brief      軸のサーボを制御した結果クラス
			///				⇒ 軸のサーボを制御した結果の基礎クラスから継承
			////////////////////////////////////////////////////////////////////////////////
			class CServo
				: virtual public Result::Servo::CBasis
			{
			public:
				////////////////////////////////////////////////////////////////////////////////
				/// @brief			コンストラクタ
				////////////////////////////////////////////////////////////////////////////////
				CServo() : CBasis()
				{

				}

				////////////////////////////////////////////////////////////////////////////////
				/// @brief			コピー代入演算子
				/// @return			自クラス
				////////////////////////////////////////////////////////////////////////////////
				CServo& operator = (const CServo& object) noexcept
				{
					// 更新
					Update(object);

					return *this;
				}

				////////////////////////////////////////////////////////////////////////////////
				/// @brief			デストラクタ
				////////////////////////////////////////////////////////////////////////////////
				~CServo() override
				{

				}

			protected:
				////////////////////////////////////////////////////////////////////////////////
				/// @brief			自クラスを更新
				/// @param[in]		object	自クラス
				////////////////////////////////////////////////////////////////////////////////
				void Update(const CServo& object)
				{
					// 既定の関数
					Result::Servo::CBasis::Update(object);
				}
			};
		}
	}
}